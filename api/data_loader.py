# Cache parsed quality report to avoid re-reading on every request
_quality_cache: dict[str, dict] = {}


def clear_quality_cache(version: str | None = None) -> None:
    """Invalidate quality report cache. Call after quality sweep generates new reports."""
    if version:
        _quality_cache.pop(version, None)
    else:
        _quality_cache.clear()


def _parse_quality_report(version: str = LATEST_VERSION) -> dict[str, dict]:
    """Parse output/quality_report_{version}.md → {sample_id: {scores}}."""
    version = resolve_version(version)
    if version in _quality_cache:
        return _quality_cache[version]

    path = OUTPUT_DIR / f"quality_report_{version}.md"
    if not path.exists():
        return {}

    text = path.read_text()
    result: dict[str, dict] = {}

    for line in text.splitlines():
        if not line.startswith("|") or "---" in line or "Sample" in line:
            continue
        parts = [p.strip() for p in line.strip("|").split("|")]
        if len(parts) < 7:
            continue
        sample_id = parts[0]
        if not sample_id or sample_id == "Sample":
            continue
        try:
            result[sample_id] = {
                "overall": float(parts[1]) if parts[1] and parts[1] != "—" else None,
                "accuracy": float(parts[2]) if parts[2] and parts[2] != "—" else None,
                "completeness": float(parts[3]) if parts[3] and parts[3] != "—" else None,
                "no_hallucination": float(parts[4]) if parts[4] and parts[4] != "—" else None,
                "structure": float(parts[5]) if parts[5] and parts[5] != "—" else None,
                "language": float(parts[6]) if parts[6] and parts[6] != "—" else None,
                "overlap": parts[7].replace("%", "") if len(parts) > 7 and "%" in parts[7] else None,
            }
        except (ValueError, IndexError):
            continue

    _quality_cache[version] = result
    return result


def _get_sample_scores(sample_id: str, version: str = LATEST_VERSION) -> Optional[dict]:
    version = resolve_version(version)
    scores = _parse_quality_report(version)
    return scores.get(sample_id)


def get_aggregate_quality(version: str = LATEST_VERSION) -> dict:
    """Return aggregate quality stats for all samples."""
    version = resolve_version(version)
    scores_map = _parse_quality_report(version)
    if not scores_map:
        return {}

    all_scores = [v["overall"] for v in scores_map.values() if v.get("overall")]
    if not all_scores:
        return {}

    dims = ["accuracy", "completeness", "no_hallucination", "structure", "language"]
    dim_avgs = {}
    for d in dims:
        vals = [v[d] for v in scores_map.values() if v.get(d)]
        dim_avgs[d] = round(sum(vals) / len(vals), 3) if vals else None

    return {
        "version": version,
        "sample_count": len(all_scores),
        "average": round(sum(all_scores) / len(all_scores), 3),
        "min": min(all_scores),
        "max": max(all_scores),
        "dimensions": dim_avgs,
    }


def get_quality_by_version() -> list[dict]:
    """Return quality trend across all available versions."""
    trend = []
    for v in reversed(get_versions()):  # v1 → v7 order
        agg = get_aggregate_quality(v)
        if agg:
            trend.append(agg)
    return trend


def get_aggregate_quality_by_mode(version: str = LATEST_VERSION) -> dict:
    """Return aggregate quality stats broken down by dictation vs ambient."""
    version = resolve_version(version)
    scores_map = _parse_quality_report(version)
    if not scores_map:
        return {}

    samples_list = list_samples()
    sample_modes = {s["sample_id"]: s["mode"] for s in samples_list}

    result = {}
    for mode_label in ("dictation", "ambient"):
        mode_scores = {
            sid: sc for sid, sc in scores_map.items()
            if sample_modes.get(sid) == mode_label and sc.get("overall")
        }
        if not mode_scores:
            continue
        all_overall = [sc["overall"] for sc in mode_scores.values()]
        dims = ["accuracy", "completeness", "no_hallucination", "structure", "language"]
        dim_avgs = {}
        for d in dims:
            vals = [sc[d] for sc in mode_scores.values() if sc.get(d)]
            dim_avgs[d] = round(sum(vals) / len(vals), 3) if vals else None

        result[mode_label] = {
            "version": version,
            "sample_count": len(all_overall),
            "average": round(sum(all_overall) / len(all_overall), 3),
            "min": min(all_overall),
            "max": max(all_overall),
            "dimensions": dim_avgs,
        }
    return result


def get_aggregate_quality_by_provider(version: str = LATEST_VERSION) -> list[dict]:
    """Return aggregate quality per provider."""
    version = resolve_version(version)
    scores_map = _parse_quality_report(version)
    if not scores_map:
        return []

    samples_list = list_samples()
    # Group samples by physician
    physician_samples: dict[str, list[str]] = {}
    for s in samples_list:
        phys = s.get("physician", "unknown")
        physician_samples.setdefault(phys, []).append(s["sample_id"])

    result = []
    for phys, sample_ids in sorted(physician_samples.items()):
        phys_scores = [
            scores_map[sid]["overall"]
            for sid in sample_ids
            if sid in scores_map and scores_map[sid].get("overall")
        ]
        if not phys_scores:
            continue
        display_name = phys.replace("_", " ").title()
        result.append({
            "provider_id": phys,
            "provider_name": display_name,
            "sample_count": len(phys_scores),
            "average": round(sum(phys_scores) / len(phys_scores), 3),
            "min": min(phys_scores),
            "max": max(phys_scores),
        })
    return result


def get_all_sample_scores(version: str = LATEST_VERSION) -> list[dict]:
    """Return per-sample scores as a list for the samples table."""
    version = resolve_version(version)
    scores_map = _parse_quality_report(version)
    result = []
    for sample_id, scores in scores_map.items():
        mode = get_mode(sample_id)
        result.append({"sample_id": sample_id, "mode": mode, "version": version, **scores})
    return result


