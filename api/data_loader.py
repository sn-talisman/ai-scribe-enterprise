def get_patient_context(sample_id: str) -> Optional[dict]:
    """
    Load patient context from the new JSON files:
      patient_demographics.json  +  encounter_details.json

    Returns a unified dict matching the old patient_context.yaml shape
    so the UI and pipeline continue to work unchanged.
    """
    result = _data_dir_for(sample_id)
    if result is None:
        return None
    _mode, _phys, data_dir = result

    demo_path = data_dir / "patient_demographics.json"
    enc_path = data_dir / "encounter_details.json"

    if not demo_path.exists() and not enc_path.exists():
        return None

    demo = {}
    enc = {}
    if demo_path.exists():
        demo = json.loads(demo_path.read_text())
    if enc_path.exists():
        enc = json.loads(enc_path.read_text())

    # Build a unified context dict compatible with existing UI expectations
    provider = enc.get("provider", {})
    return {
        "patient": {
            "name": f"{demo.get('first_name', '')} {demo.get('last_name', '')}".strip() or None,
            "date_of_birth": demo.get("date_of_birth"),
            "sex": None,  # not in new demographics JSON
            "mrn": demo.get("record_number"),
        },
        "encounter": {
            "date_of_service": enc.get("date_of_exam"),
            "visit_type": enc.get("visit_type"),
            "date_of_injury": enc.get("date_of_accident") or None,
            "case_number": enc.get("case_number") or None,
            "encounter_id": enc.get("encounter_id"),
            "mode": enc.get("mode"),
            "location": enc.get("location"),
        },
        "provider": {
            "name": provider.get("full_name"),
            "credentials": None,
            "specialty": None,
        },
        "facility": {
            "name": None,
            "location": enc.get("location"),
        },
    }


def get_mode(sample_id: str) -> str:
    result = _data_dir_for(sample_id)
    if result is not None:
        mode, _phys, _dir = result
        return "ambient" if mode == "conversation" else "dictation"
    # Fallback: check output dir
    for mode, _phys, sid, _dir in _walk_encounters(OUTPUT_DIR):
        if sid == sample_id:
            return "ambient" if mode == "conversation" else "dictation"
    return "dictation"


