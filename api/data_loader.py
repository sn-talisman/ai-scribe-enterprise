"""
api/data_loader.py

Reads the output/ and ai-scribe-data/ directory structures to serve encounter
data via the FastAPI routes.  No database required — all data lives in Markdown
and JSON files generated by the pipeline.

New folder hierarchy (March 2026):
  ai-scribe-data/
    conversation/<physician>/<patient>_<eid>_<date>/
      patient_demographics.json, encounter_details.json,
      conversation_audio.mp3, note_audio.mp3, final_soap_note.md
    dictation/<physician>/<patient>_<eid>_<date>/
      patient_demographics.json, encounter_details.json,
      dictation.mp3, final_soap_note.md

  output/
    conversation/<physician>/<patient>_<eid>_<date>/
      generated_note_v{N}.md, comparison_v{N}.md, audio_transcript_v{N}.txt
    dictation/<physician>/<patient>_<eid>_<date>/
      ...
"""
from __future__ import annotations

import json
