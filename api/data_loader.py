def list_providers() -> list[dict]:
    if not PROVIDERS_DIR.exists():
        return []
    result = []
    for yaml_path in sorted(PROVIDERS_DIR.glob("*.yaml")):
        with open(yaml_path) as f:
            data = yaml.safe_load(f)
        result.append(_summarize_provider(data))
    return result


def get_provider(provider_id: str) -> Optional[dict]:
    path = PROVIDERS_DIR / f"{provider_id}.yaml"
    if not path.exists():
        return None
    with open(path) as f:
        return yaml.safe_load(f)


def _summarize_provider(data: dict) -> dict:
    latest_score = None
    if data.get("quality_scores"):
        scores = data["quality_scores"]
        for v in VERSIONS:
            if v in scores:
                latest_score = scores[v]
                break
    return {
        "id": data.get("id"),
        "name": data.get("name"),
        "credentials": data.get("credentials"),
        "specialty": data.get("specialty"),
        "latest_score": latest_score,
        "quality_scores": data.get("quality_scores", {}),
    }
