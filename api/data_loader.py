"""
api/data_loader.py

Reads the output/ and data/ directory structure to serve encounter data
via the FastAPI routes. No database required — all data lives in Markdown
and YAML files generated by the pipeline.
"""
from __future__ import annotations

import re
from pathlib import Path
from typing import Optional

import yaml

# Repository root (two levels up from this file)
ROOT = Path(__file__).parent.parent
OUTPUT_DIR = ROOT / "output"
DATA_DIR = ROOT / "data"
PROVIDERS_DIR = ROOT / "config" / "providers"

VERSIONS = ["v6", "v5", "v4", "v3", "v2", "v1"]
LATEST_VERSION = "v6"


# ---------------------------------------------------------------------------
# Sample discovery
# ---------------------------------------------------------------------------

def _mode_for(output_subdir: str) -> str:
    """Map output subdirectory name → mode label."""
    return "dictation" if "dictation" in output_subdir else "ambient"


def _data_subdir(mode: str) -> str:
    return "dictation" if mode == "dictation" else "conversations"


def list_samples() -> list[dict]:
    """Return all samples found in output/, with metadata."""
    samples = []

    for subdir in ("dictation", "conversations"):
        base = OUTPUT_DIR / subdir
        if not base.exists():
            continue
        for sample_dir in sorted(base.iterdir()):
            if not sample_dir.is_dir():
                continue
            sample_id = sample_dir.name
            mode = _mode_for(subdir)

            # Determine which versions exist
            versions_available = [
                v for v in VERSIONS
                if (sample_dir / f"generated_note_{v}.md").exists()
            ]
            has_gold = _has_gold(sample_id, mode)

            # Quality scores from latest quality report
            scores = _get_sample_scores(sample_id)

            samples.append({
                "sample_id": sample_id,
                "mode": mode,
                "versions": versions_available,
                "latest_version": versions_available[0] if versions_available else None,
                "has_gold": has_gold,
                "quality": scores,
            })

    return samples


def _has_gold(sample_id: str, mode: str) -> bool:
    data_base = DATA_DIR / _data_subdir(mode)
    return (
        (data_base / sample_id / "soap_final.md").exists()
        or (data_base / sample_id / "soap_initial.md").exists()
    )


# ---------------------------------------------------------------------------
# Note / comparison / transcript content
# ---------------------------------------------------------------------------

def get_generated_note(sample_id: str, version: str = LATEST_VERSION) -> Optional[str]:
    for subdir in ("dictation", "conversations"):
        path = OUTPUT_DIR / subdir / sample_id / f"generated_note_{version}.md"
        if path.exists():
            return path.read_text()
    return None


def get_transcript(sample_id: str, version: str = LATEST_VERSION) -> Optional[str]:
    """Return the standalone transcript text for a sample + version."""
    for subdir in ("dictation", "conversations"):
        path = OUTPUT_DIR / subdir / sample_id / f"audio_transcript_{version}.txt"
        if path.exists():
            return path.read_text()
    return None


def list_transcript_versions(sample_id: str) -> list[str]:
    """Return all versions that have a standalone transcript file."""
    for subdir in ("dictation", "conversations"):
        sample_dir = OUTPUT_DIR / subdir / sample_id
        if sample_dir.exists():
            versions = []
            for v in VERSIONS:
                if (sample_dir / f"audio_transcript_{v}.txt").exists():
                    versions.append(v)
            return versions
    return []


def get_audio_path(sample_id: str) -> Optional[str]:
    """Return absolute path to the audio file for a sample (for streaming)."""
    for subdir, audio_name in (("dictation", "dictation.mp3"), ("conversations", "conversation.mp3")):
        path = DATA_DIR / subdir / sample_id / audio_name
        if path.exists():
            return str(path)
    return None


def get_comparison(sample_id: str, version: str = LATEST_VERSION) -> Optional[str]:
    for subdir in ("dictation", "conversations"):
        path = OUTPUT_DIR / subdir / sample_id / f"comparison_{version}.md"
        if path.exists():
            return path.read_text()
    return None


def get_gold_note(sample_id: str) -> Optional[str]:
    for mode, subdir in (("dictation", "dictation"), ("ambient", "conversations")):
        base = DATA_DIR / subdir / sample_id
        for fname in ("soap_final.md", "soap_initial.md"):
            path = base / fname
            if path.exists():
                return path.read_text()
    return None


def get_patient_context(sample_id: str) -> Optional[dict]:
    for subdir in ("dictation", "conversations"):
        path = DATA_DIR / subdir / sample_id / "patient_context.yaml"
        if path.exists():
            with open(path) as f:
                return yaml.safe_load(f)
    return None


def get_mode(sample_id: str) -> str:
    if (OUTPUT_DIR / "dictation" / sample_id).exists():
        return "dictation"
    return "ambient"


