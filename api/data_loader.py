def list_providers() -> list[dict]:
    """List all providers — from YAML profiles AND from data directory physicians."""
    seen: set[str] = set()
    result = []

    # 1. Providers with YAML profiles
    if PROVIDERS_DIR.exists():
        for yaml_path in sorted(PROVIDERS_DIR.glob("*.yaml")):
            with open(yaml_path) as f:
                data = yaml.safe_load(f)
            result.append(_summarize_provider(data))
            seen.add(data.get("id", yaml_path.stem))

    # 2. Discover physicians from data directories that don't have profiles
    for base_dir in (DATA_DIR, OUTPUT_DIR):
        if not base_dir.exists():
            continue
        for mode in _MODES:
            mode_dir = base_dir / mode
            if not mode_dir.exists():
                continue
            for physician_dir in sorted(mode_dir.iterdir()):
                if not physician_dir.is_dir():
                    continue
                pid = physician_dir.name
                if pid in seen:
                    continue
                seen.add(pid)
                display_name = pid.replace("_", " ").title()
                result.append({
                    "id": pid,
                    "name": display_name,
                    "credentials": None,
                    "specialty": None,
                    "latest_score": None,
                    "quality_scores": {},
                })

    # Attach avg quality scores for all providers from latest quality report
    scores_map = _parse_quality_report("latest")
    if scores_map:
        samples_list = list_samples()
        for provider in result:
            pid = provider["id"]
            provider_sample_ids = [s["sample_id"] for s in samples_list if s.get("physician") == pid]
            provider_scores = [
                scores_map[sid]["overall"]
                for sid in provider_sample_ids
                if sid in scores_map and scores_map[sid].get("overall")
            ]
            if provider_scores and provider.get("latest_score") is None:
                provider["latest_score"] = round(sum(provider_scores) / len(provider_scores), 2)

    return result


def get_provider(provider_id: str) -> Optional[dict]:
    path = PROVIDERS_DIR / f"{provider_id}.yaml"
    if not path.exists():
        return None
    with open(path) as f:
        return yaml.safe_load(f)


def _summarize_provider(data: dict) -> dict:
    latest_score = None
    if data.get("quality_scores"):
        scores = data["quality_scores"]
        for v in get_versions():
            if v in scores:
                latest_score = scores[v]
                break
    return {
        "id": data.get("id"),
        "name": data.get("name"),
        "credentials": data.get("credentials"),
        "specialty": data.get("specialty"),
        "latest_score": latest_score,
        "quality_scores": data.get("quality_scores", {}),
    }
