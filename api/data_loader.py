# Encounter discovery helpers
# ---------------------------------------------------------------------------

def _walk_encounters(base_dir: Path) -> list[tuple[str, str, str, Path]]:
    """
    Walk ai-scribe-data/ (or output/) and yield
    (mode, physician, sample_id, encounter_dir)
    for every encounter folder found.

    Layout: base_dir/<mode>/<physician>/<encounter_folder>/
    """
    results = []
    for mode in _MODES:
        mode_dir = base_dir / mode
        if not mode_dir.exists():
            continue
        for physician_dir in sorted(mode_dir.iterdir()):
            if not physician_dir.is_dir():
                continue
            physician = physician_dir.name
            for encounter_dir in sorted(physician_dir.iterdir()):
                if not encounter_dir.is_dir():
                    continue
                sample_id = encounter_dir.name
                results.append((mode, physician, sample_id, encounter_dir))
    return results


def _output_dir_for(sample_id: str) -> Optional[Path]:
    """Find the output directory for a given sample_id."""
    for mode, physician, sid, enc_dir in _walk_encounters(OUTPUT_DIR):
        if sid == sample_id:
            return enc_dir
    return None


def _data_dir_for(sample_id: str) -> Optional[tuple[str, str, Path]]:
    """Find (mode, physician, data_dir) for a sample_id in ai-scribe-data/."""
    for mode, physician, sid, enc_dir in _walk_encounters(DATA_DIR):
        if sid == sample_id:
            return mode, physician, enc_dir
    return None


