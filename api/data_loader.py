def list_samples() -> list[dict]:
    """Return all samples found in output/ and ai-scribe-data/, with metadata.

    Samples from ai-scribe-data/ that don't have output yet still appear
    (with empty versions) so the UI can display all encounters.
    """
    seen: set[str] = set()
    samples = []

    # 1. Walk output/ first (these have generated notes)
    for mode, physician, sample_id, out_dir in _walk_encounters(OUTPUT_DIR):
        seen.add(sample_id)
        versions_available = _discover_sample_versions(out_dir)
        has_gold = _has_gold(sample_id)
        scores = _get_sample_scores(sample_id)
        display_mode = "ambient" if mode == "conversation" else "dictation"

        samples.append({
            "sample_id": sample_id,
            "mode": display_mode,
            "physician": physician,
            "versions": versions_available,
            "latest_version": versions_available[0] if versions_available else None,
            "has_gold": has_gold,
            "quality": scores,
            "is_test": _is_test_data(physician, sample_id),
        })

    # 2. Walk ai-scribe-data/ for encounters not yet in output/
    for mode, physician, sample_id, data_dir in _walk_encounters(DATA_DIR):
        if sample_id in seen:
            continue
        seen.add(sample_id)
        has_gold = (data_dir / "final_soap_note.md").exists()
        display_mode = "ambient" if mode == "conversation" else "dictation"

        samples.append({
            "sample_id": sample_id,
            "mode": display_mode,
            "physician": physician,
            "versions": [],
            "latest_version": None,
            "has_gold": has_gold,
            "quality": None,
            "is_test": _is_test_data(physician, sample_id),
        })

    return samples


def _is_test_data(physician: str, sample_id: str) -> bool:
    """Return True if this encounter is test data (not real patient data)."""
    return "test" in physician.lower() or "test" in sample_id.lower()


def _has_gold(sample_id: str) -> bool:
    result = _data_dir_for(sample_id)
    if result is None:
        return False
    _mode, _phys, data_dir = result
    return (data_dir / "final_soap_note.md").exists()


