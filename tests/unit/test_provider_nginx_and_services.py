# Tests — TLS cert paths in nginx config (Req 3.2)
# ---------------------------------------------------------------------------

class TestNginxTlsPaths:
    def test_cert_path_in_config(self):
        cert = "C:/certs/my-provider.crt"
        conf = generate_nginx_conf(cert, "/key.pem")
        assert f"ssl_certificate     {cert}" in conf

    def test_key_path_in_config(self):
        key = "C:/certs/my-provider.key"
        conf = generate_nginx_conf("/cert.pem", key)
        assert f"ssl_certificate_key {key}" in conf

    def test_backslash_paths_normalized(self):
        """Windows backslash paths should be converted to forward slashes."""
        cert = r"C:\nginx\ssl\cert.pem"
        key = r"C:\nginx\ssl\key.pem"
        conf = generate_nginx_conf(cert, key)
        assert "C:/nginx/ssl/cert.pem" in conf
        assert "C:/nginx/ssl/key.pem" in conf
        assert "\\" not in conf.split("ssl_certificate")[1].split("\n")[0]

    def test_arbitrary_paths_appear_verbatim(self):
        cert = "/opt/custom/location/server.crt"
        key = "/opt/custom/location/server.key"
        conf = generate_nginx_conf(cert, key)
        assert f"ssl_certificate     {cert}" in conf
        assert f"ssl_certificate_key {key}" in conf


