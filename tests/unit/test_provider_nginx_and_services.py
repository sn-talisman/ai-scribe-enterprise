def generate_nginx_conf(tls_cert_path: str, tls_key_path: str) -> str:
    """
    Python equivalent of the PowerShell nginx.conf generator (Step 5/7).
    Generates an nginx configuration string with HTTPS, reverse proxy,
    WebSocket upgrade, X-Forwarded-Proto, and rate limiting.
    """
    # Normalize paths for nginx (forward slashes)
    cert = tls_cert_path.replace("\\", "/")
    key = tls_key_path.replace("\\", "/")

    return f"""\
# AI Scribe Provider Server - nginx configuration
# Generated by deploy/setup_provider_server.ps1

worker_processes auto;

events {{
    worker_connections 1024;
}}

http {{
    include       mime.types;
    default_type  application/octet-stream;
    sendfile      on;
    keepalive_timeout 65;

    # Rate limiting: 100 requests/second per client IP
    limit_req_zone $binary_remote_addr zone=api_limit:10m rate=100r/s;

    # Upstream backends
    upstream api_backend {{
        server 127.0.0.1:8000;
    }}

    upstream web_backend {{
        server 127.0.0.1:3000;
    }}

    # Redirect HTTP to HTTPS
    server {{
        listen 80;
        server_name _;
        return 301 https://$host$request_uri;
    }}

    # HTTPS server
    server {{
        listen 443 ssl;
        server_name _;

        # TLS configuration
        ssl_certificate     {cert};
        ssl_certificate_key {key};
        ssl_protocols       TLSv1.2 TLSv1.3;
        ssl_ciphers         HIGH:!aNULL:!MD5;
        ssl_prefer_server_ciphers on;

        # WebSocket upgrade for /ws/ paths
        location /ws/ {{
            proxy_pass http://api_backend;
            proxy_http_version 1.1;
            proxy_set_header Upgrade $http_upgrade;
            proxy_set_header Connection "upgrade";
            proxy_set_header Host $host;
            proxy_set_header X-Real-IP $remote_addr;
            proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
            proxy_set_header X-Forwarded-Proto https;
            proxy_read_timeout 86400;
        }}

        # API reverse proxy
        location /api/ {{
            limit_req zone=api_limit burst=50 nodelay;
            proxy_pass http://api_backend;
            proxy_set_header Host $host;
            proxy_set_header X-Real-IP $remote_addr;
            proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
            proxy_set_header X-Forwarded-Proto https;
        }}

        # Web UI reverse proxy (default)
        location / {{
            proxy_pass http://web_backend;
            proxy_set_header Host $host;
            proxy_set_header X-Real-IP $remote_addr;
            proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
            proxy_set_header X-Forwarded-Proto https;
        }}
    }}
}}
"""


def generate_self_signed_cert_paths(project_root: str) -> dict[str, str]:
    """
    Python equivalent of the self-signed certificate path generation.
    Returns the paths that would be created by the PowerShell script.
    """
    nginx_dir = os.path.join(project_root, "deploy", "nginx")
    ssl_dir = os.path.join(nginx_dir, "ssl")
    return {
        "pfx": os.path.join(ssl_dir, "ai-scribe.pfx"),
        "cert": os.path.join(ssl_dir, "ai-scribe.crt"),
        "key": os.path.join(ssl_dir, "ai-scribe.key"),
        "ssl_dir": ssl_dir,
        "nginx_dir": nginx_dir,
    }


def build_nssm_commands(project_root: str, node_exe: str = "node") -> list[dict]:
    """
    Python equivalent of the NSSM service registration commands (Step 6/7).
    Returns a list of NSSM command descriptors for both services.
    """
    uvicorn_exe = os.path.join(project_root, ".venv", "Scripts", "uvicorn.exe")
    web_app_dir = os.path.join(project_root, "client", "web")
    env_file = os.path.join(project_root, ".env.provider")

    commands = []

    # ai-scribe-api service
    commands.append({
        "action": "install",
        "service_name": "ai-scribe-api",
        "executable": uvicorn_exe,
        "app_parameters": "api.main:app --host 0.0.0.0 --port 8000",
        "app_directory": project_root,
        "start_type": "SERVICE_AUTO_START",
        "restart_delay": 5000,
        "env_file": env_file,
        "depends_on": None,
    })

    # ai-scribe-web service
    commands.append({
        "action": "install",
        "service_name": "ai-scribe-web",
        "executable": node_exe,
        "app_parameters": "node_modules/.bin/next start -p 3000",
        "app_directory": web_app_dir,
        "start_type": "SERVICE_AUTO_START",
        "restart_delay": 5000,
        "env_file": env_file,
        "depends_on": "ai-scribe-api",
    })

    return commands


