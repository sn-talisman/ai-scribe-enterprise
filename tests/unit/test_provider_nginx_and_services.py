# Helper — Health check response validator (Python equivalent of PowerShell
# Test-HealthEndpoint logic)
# ---------------------------------------------------------------------------


def validate_health_response(response: object) -> bool:
    """
    Python equivalent of the PowerShell Test-HealthEndpoint response validator.
    Returns True iff the response is a dict-like object with a top-level
    "status" field whose value is exactly "ok".

    This mirrors the PowerShell logic:
        if ($response.status -eq "ok") { return $true }
        return $false
    """
    try:
        if isinstance(response, dict):
            return response.get("status") == "ok"
        return False
    except Exception:
        return False


