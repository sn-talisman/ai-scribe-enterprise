# Tests — Health check response validation (Req 5.1, 5.2, 5.3, 5.4, 5.5)
# ---------------------------------------------------------------------------

class TestHealthCheckValidation:
    """Tests for the health check response validator used in Step 7/7."""

    def test_status_ok_returns_true(self):
        """Response with {"status": "ok"} should return True."""
        assert validate_health_response({"status": "ok"}) is True

    def test_status_error_returns_false(self):
        """Response with {"status": "error"} should return False."""
        assert validate_health_response({"status": "error"}) is False

    def test_missing_status_field_returns_false(self):
        """Response with no status field should return False."""
        assert validate_health_response({"healthy": True}) is False

    def test_empty_dict_returns_false(self):
        """Empty response dict should return False."""
        assert validate_health_response({}) is False

    def test_connection_failure_returns_false(self):
        """Non-dict response (simulating connection failure / bad parse) returns False."""
        assert validate_health_response(None) is False

    def test_string_response_returns_false(self):
        """A plain string response should return False."""
        assert validate_health_response("ok") is False

    def test_status_ok_with_extra_fields(self):
        """Response with status ok plus extra fields should still return True."""
        assert validate_health_response({"status": "ok", "role": "provider-facing", "uptime": 123}) is True

    def test_status_none_returns_false(self):
        """Response with status: null should return False."""
        assert validate_health_response({"status": None}) is False

    def test_numeric_status_returns_false(self):
        """Response with numeric status should return False."""
        assert validate_health_response({"status": 200}) is False

    def test_list_response_returns_false(self):
        """A list response should return False."""
        assert validate_health_response([{"status": "ok"}]) is False
